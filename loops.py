#Loops
#for-loops

fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)

# while-loops
#Using a while loop to print numbers from 1 to 5

count = 1
while count <= 5:
    print("Count is:", count)
    count += 1 # Increment of the count by 1