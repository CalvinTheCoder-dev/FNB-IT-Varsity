my_dictionary = {"apple": 1, "banana": 5, "cherry": 2}
print(my_dictionary["apple"])
# Output: 1

my_dictionary["grape"] = 4
print(my_dictionary)
# Output: {'apple': 1, 'banana': 5, 'cherry': 2, 'grape': 4}

my_dictionary["banana"] = 7
print(my_dictionary)
# Output: {'apple': 1, 'banana': 7, 'cherry': 2, 'grape': 4}

