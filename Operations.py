#Operations

# Addition (+)
# Subtraction (-)
# Multiplication (*)
# Division (/)
# Modulus (%)
# Exponentiation (**)

x = 10
y = 2

print(x+y)  # Addition
print(x-y)  # Subtraction   
print(x*y)  # Multiplication
print(x/y)  # Division
print(x%y)  # Modulus
print(x**y) # Exponentiation

x += 2
y -= 1

print(x)  # Increment x by 2
print(y)  # Decrement y by 1